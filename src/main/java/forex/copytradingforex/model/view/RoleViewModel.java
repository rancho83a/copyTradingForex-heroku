package forex.copytradingforex.model.view;

public class RoleViewModel {
    private Long id;
    private String role;

    public Long getId() {
        return id;
    }

    public RoleViewModel setId(Long id) {
        this.id = id;
        return this;
    }

    public String getRole() {
        return role;
    }

    public RoleViewModel setRole(String role) {
        this.role = role;
        return this;
    }
}
